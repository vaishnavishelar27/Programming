#include<stdio.h>
////////////////////////////////////////////////////////////////////////
//
// Function name: FhtoCs
// Input:         float
// Output:        double
// Description:   Convert temperature from Fahrenheit to Celsius
//                 Formula: C = (F - 32) * (5/9)
// Date:          27/05/2026
// Author:        Vaishnavi Vijay Shelar
//
////////////////////////////////////////////////////////////////////////
double FhtoCs(float fTemp)
{
    double dCelsius = 0.0;
    dCelsius = (fTemp - 32.0) * (5.0 / 9.0);
    return dCelsius;
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform check the temperature
//
////////////////////////////////////////////////////////////////////////
int main()
{
    float fValue = 0.0;
    double dRet = 0.0;
    printf("Enter temperature in Fahrenheit: ");
    scanf("%f",&fValue);

    dRet = FhtoCs(fValue);
    printf("%.4lf",dRet);
    return 0;
}
