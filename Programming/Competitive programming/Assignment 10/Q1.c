////////////////////////////////////////////////////////////////////////
//
// Function name: CircleArea
// Input:         float
// Output:        double
// Description:   Calculate area of circle
// Date:          27/05/2026
// Author:        Vaishnavi Vijay Shelar
//
////////////////////////////////////////////////////////////////////////
double CircleArea(float fRedius)
{
    float fPI = 3.14f;
    double dAns = 0.0;

    dAns = fPI * fRedius * fRedius;

    return dAns;
}

//////////////////////////////////////////////////////////////////////// 
//
// Application to perform and calculate area of circle
//
////////////////////////////////////////////////////////////////////////
int main()
{
    float fValue = 0.0f;
    double dRet = 0.0;
    printf("Enter number: ");
    scanf("%f",&fValue);
    dRet = CircleArea(fValue);
    printf("%.4lf\n",dRet); 
    return 0;
}
