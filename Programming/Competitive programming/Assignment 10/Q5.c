#include<stdio.h>
////////////////////////////////////////////////////////////////////////
//
// Function name: SquareMeter
// Input:         int
// Output:        double
// Description:   Convert area from square feet to square meters
//                 (1 sq ft = 0.092903 sq m)
// Date:          27/05/2026
// Author:        Vaishnavi Vijay Shelar
//
////////////////////////////////////////////////////////////////////////
double SquareMeter(int iValue)
{
    double dAns = 0.0;
    dAns = iValue * 0.092903;
    return dAns;
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform calculate square meter
//
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    double dRet = 0.0;
    printf("Enter area in square feet: ");
    scanf("%d",&iValue);
    dRet = SquareMeter(iValue);
    printf("%.6lf",dRet);
    return 0;
}
