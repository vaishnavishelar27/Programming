//Function name: NonFact
//Input:         int
//Output:        int
//Description:   Display the all non factors of given number
//Date:          23/05/2026
//Author:        Vaishnavi Vijay Shelar
//
//////////////////////////////////////////////////////////////////////// 
void NonFact(int iNo)
{
    int iCnt=0;
    if(iNo<=0)
    {
        iNo=-iNo;
    }
    for(iCnt=1;iCnt<iNo;iCnt++)
    {
        if(iNo%iCnt != 0)
        {
            printf("%d\t",iCnt);
        }
    }
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform Display the all non factors
//
//
//////////////////////////////////////////////////////////////////////// 
int main()
{
    int iValue=0;
    printf("Enter number: ");
    scanf("%d",&iValue);
    NonFact(iValue);

    return 0;
}
