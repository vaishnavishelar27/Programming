void PrintEven(int iNo)
{
    if(iNo<=0)
    {
        return;
    }
    for(int iCnt=1;iCnt<=iNo;iCnt++)
    {
        printf("%d\t",iCnt*2);
    }
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform and accept one number from user and print 
//  number of even number on screen
//
//
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0;
    printf("Enter number\n: ");
    scanf("%d",&iValue);
    PrintEven(iValue);
    return 0;
}
