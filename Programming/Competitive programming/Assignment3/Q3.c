void DisplayEvenFactor(int iNo)
{
    int i =0;
    if(iNo<=0)
    {
        iNo=-iNo;
    }
    for(i=1;i<=iNo/2;i++)
    {
        if((iNo%i ==0 )&& (i%2==0))
        {
            printf("%d\t",i);
        }
    }
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform and accept number from user and display even 
// factor of given number
//
//
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0;
    printf("Enter number");
    scanf("%f",&iValue);
    DisplayEvenFactor(iValue);
    return 0;
}
