////////////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    //updater
    while(iNo>0)
    {
        printf(" * ");
        iNo--;

    }
    
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform and print * on screen
//
//
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0;
    printf("Enter number: ");
    scanf("%d",&iValue);

    Display(iValue);
    return 0;
}
