BOOL ChkEven(int iNo)
{
    if((iNo%2)==0)
    {
        printf("FALSE");
    }
    else
    {
        printf("TRUE");
    }
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform check the even or number if number is even return FALSE
// else return TRUE
//
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0;
    BOOL bRet=FALSE;
    printf("Enter number: ");
    scanf("%d",&iValue);
    bRet=ChkEven(iValue);
    return 0;
}
