#include<stdio.h>
////////////////////////////////////////////////////////////////////////
//
// Function name: KMtoMeter
// Input:         int
// Output:        int
// Description:   Convert distance from kilometers to meters
//                 (1 kilometer = 1000 meters)
// Date:          27/05/2026
// Author:        Vaishnavi Vijay Shelar
//
////////////////////////////////////////////////////////////////////////
int KMtoMeter(int iNo)
{
    int iResult = 0;
    iResult = iNo * 1000;
    return iResult;
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform and calculate kilometers to meter
//
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    int iRet = 0;
    printf("Enter distance: ");
    scanf("%d",&iValue);
    iRet = KMtoMeter(iValue);
    printf("%d\n",iRet);
    return 0;
}
