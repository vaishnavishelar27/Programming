void Display(int iNo)
{
    int iCnt=0;
    while(iCnt<iNo)
    {
        printf("*");

        iCnt++;
    }
    
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform and print *
//
//
////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0;
    printf("Enter value: ");
    scanf("%d",&iValue);
    Display(iValue);
    return 0;
}
