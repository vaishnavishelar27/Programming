//Function name: DisplayCovert
//Input:         char
//Output:        char
//Description:   Perform and check to conver character uppercase to lowercase
//                and lowercase to uppercase
//Date:          20/05/2026
//Author:        Vaishnavi Vijay Shelar
//
////////////////////////////////////////////////////////////////////////
void DisplayCovert(char cValue)
{
    if(cValue>='a' && cValue<='z')
    {
        printf("%c",cValue-32);
    }
    else if(cValue>='A' && cValue<='Z')
    {
        printf("%c",cValue+32);
    }
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform and get character by user if character in lowercase 
// output will be in uppercase else input will be uppercase output will be in 
// lowercase
//
////////////////////////////////////////////////////////////////////////
int main()
{
    char cValue='\0';
    printf("Enter character");
    scanf("%c",&cValue);
    DisplayCovert(cValue);
    return 0;
}
