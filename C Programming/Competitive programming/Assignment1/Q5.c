void Accept(int iNo)
{
    int iCnt =0;
    for(iCnt=1;iCnt<=iNo;iCnt++)
    {
        printf("*");
    }
}
//////////////////////////////////////////////////////////////////////// 
//
// Application to perform print * on screen 
//
//
//////////////////////////////////////////////////////////////////////// 
int main()
{
    int iValue=0;
    iValue=5;
    Accept(iValue);
    return 0;
}

